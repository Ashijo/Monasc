using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// Case data to json the Case object
/// </summary>
[System.Serializable]
public class CaseData {

    [SerializeField]
    private int bType; // GV.BATTLEFIELDTYPE
    [SerializeField]
    private int cType; // GV.CASETYPES
    [SerializeField]
    private Vector3 position;
    [SerializeField]
    private float height;

    [SerializeField]
    private bool spawnable;
    [SerializeField]
    private int team;

    public GV.BATTLEFIELDTYPE GetBType() { return (GV.BATTLEFIELDTYPE) bType; }
    public GV.CASETYPES GetCType() { return (GV.CASETYPES) cType; }
    public Vector3 GetPosition() { return position; }
    public float GetHeight() { return height; }
    public bool GetSpawnable() { return spawnable; }
    public int GetTeam() { return team; }

    public CaseData(GV.BATTLEFIELDTYPE bType, GV.CASETYPES cType, Vector3 position, float height) {
        this.bType = (int)bType;
        this.cType = (int)cType;
        this.position = position;
        this.height = height;


    }

    public void Update(GV.CASETYPES cType, float height, bool isSpawnable = false, int team = -1) {
        this.cType = (int)cType;
        this.height = height;
        spawnable = isSpawnable;
        this.team = team;
    }

}
